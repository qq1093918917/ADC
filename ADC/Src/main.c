  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		if(KEY0==0)
		{
			HAL_Delay(5);
			if(KEY0==0)
			{
				arm_cfft_radix4_init_f32(&scfft,FFT_LENGTH,0,1);
				
				__HAL_TIM_SetCounter(&htim3,0);
				timeout=0;
				
				for(i=0;i<FFT_LENGTH;i++)
				{
					fft_inputbuf[2*i]=(float)Get_Adc(&hadc1);
				}
				time=__HAL_TIM_GetCounter(&htim3)+(u32)timeout*65536;
				for(i=0;i<FFT_LENGTH;i++)
				{
					fft_inputbuf[2*i]=fft_inputbuf[2*i]*3.3/4096;
				}
				
				printf("采样到的值为：");
				printf("\r\n\r\n");
				for(i=0;i<FFT_LENGTH;i++)
				{
					fft_inputbuf[2*i+1]=0;
					printf("%f ",fft_inputbuf[2*i]);
				}
				arm_cfft_radix4_f32(&scfft,fft_inputbuf);	//FFT计算（基4）
				arm_cmplx_mag_f32(fft_inputbuf,fft_outputbuf,FFT_LENGTH);	//把运算结果复数求模得幅值 
				printf("傅里叶的值为：");
				printf("\r\n\r\n");
				for(i=0;i<FFT_LENGTH;i++)
				{
				printf("%f ",fft_outputbuf[i]);
				
				}
				printf("\r\n\r\n");//插入换行
				for(i=0;i<FFT_LENGTH;i++)
				{
				/*	if(fft_outputbuf[i]>=1)
					{
						printf("这是第%d个,数值为%f",i,fft_outputbuf[i]);
					printf("\r\n\r\n");//插入换行
					}*/
					fft_inputbuf[2*i]=0;
					fft_inputbuf[2*i+1]=0;
				}
				printf("总的采样时间为:%f uS",time);
				while(!KEY0);
			}
		}
		if(KEY1==0)
		{
			HAL_Delay(5);
			if(KEY1==0)
			{
				for(i=0;i<FFT_LENGTH;i++)
			{
				 fft_inputbuf[2*i]=0;
				 fft_inputbuf[2*i+1]=0;
					arm_cfft_radix4_init_f32(&scfft,FFT_LENGTH,0,1);
			}
			}
		}
  }
  /* USER CODE END 3 */

